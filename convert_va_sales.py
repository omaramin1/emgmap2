def parse_data():
    features = []

    for line in raw_data.strip().split('\n'):
        line = line.strip()
        if not line:
            continue

        parts = line.split(',')
        if len(parts) >= 7:
            try:
                address = parts[0].strip()
                city = parts[1].strip()
                state = parts[2].strip()
                zip_code = parts[3].strip()
                lat = float(parts[4].strip())
                lng = float(parts[5].strip())
                sale_date = parts[6].strip().split(' ')[0] if len(parts) > 6 else ''

                # Skip invalid coordinates
                if lat < 30 or lat > 40 or lng > -70 or lng < -80:
                    continue

                feature = {
                    "type": "Feature",
                    "geometry": {
                        "type": "Point",
                        "coordinates": [lng, lat]
                    },
                    "properties": {
                        "address": address,
                        "city": city,
                        "state": state,
                        "zip": zip_code,
                        "sale_date": sale_date,
                        "type": "complete"  # All historical sales are complete
                    }
                }
                features.append(feature)
            except (ValueError, IndexError) as e:
                continue

    return {
        "type": "FeatureCollection",
        "features": features
    }

